<b>What is the difference between SSDI and SSI?</b><br />
&nbsp;&nbsp;Social Security Disability Insurance (SSDI) is for insured workers, their disabled surviving spouses and children (disabled before the age of 22) or disabled, retired or deceased workers.  The income of other people in the family (NOT the disabled person) is irrelevant to SSDI benefits.<br /><br />

&nbsp;&nbsp;Supplemental Security Income (SSI) is for people with little or no income and resources.  People who do not have enough work credits to qualify for SSDI may be eligible for SSI based on the family's income. 
